//Second Page header fades when user hovers over it
//first header
let header1 = document.getElementById('titlesecond1');
header1.addEventListener('mouseover',function(){
    header1.style.opacity = '0.5';
})

header1.addEventListener('mouseout',function(){
    header1.style.opacity = '';
})

